						<h2> Current Ventures </h2>
						<p> Andreessen cofounded and was chairman of Ning, the third company he established after Netscape and Loudcloud. In September 2011, it was announced that Ning had been sold to Glam Media for a reported price of $150 million. Andreessen joined Glam Media’s board of directors following the sale.
						Andreessen is an investor in social news website Digg and several other technology startups such as Plazes, Netvibes, CastTV,Twitter, and the RockMelt browser launched in 2010. He is a personal investor in such diverse companies as LinkedIn and boutique bank Raine.
						Andreessen serves on the board of Facebook, eBay, Kno, Hewlett-Packard, Stanford Hospital, Bump Technologies,Anki,[44] Oculus VR, and TinyCo. He serves as advisor to Asana and director of CollabNet </p>

				

