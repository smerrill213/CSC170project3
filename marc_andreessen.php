						<h2>Industry Influence </h2>
							<p> Andreessen's experiences as an entrepreneur, investor, and board member at several large technology firms have positioned him to “draw insightful conclusions” about technology trends and help Andreessen Horowitz “stand out from the crowd” (The Economist).[28] Andreessen often advises the leaders of companies in which Andreessen Horowitz invests, including Mark Zuckerberg of Facebook and Mark Pincus of Zynga.[28]
							In 2010, notable Silicon Valley attorney Ted Wang created the first free standardized seed round financing documents, the Series Seed Documents.
							An op-ed piece Andreessen authored for The Wall Street Journal in August 2011, “Why Software Is Eating the World”, received over 445 comments and prompted articles on the topic in The Economist and other media outlets. 
							Andreessen and Horowitz were ranked no. six on Vanity Fair’s 2011 New Establishment List, no. 1 on CNET’s 2011 most influential investors list and no. 2 and 21, respectively, on the 2012 Forbes Midas List of Tech’s Top Investors.
							In April 2012, Andreessen and Andreessen Horowitz General Partners Ben Horowitz, Peter Levine, Jeff Jordan, John O’Farrell, and Scott Weiss pledged to donate half of their lifetime incomes from venture capital to charitable organizations.
							In 2012, Andreessen was named in Time 100, an annual list of the 100 most influential people in the world assembled byTime.
							In 2013, Andreessen was one of five Internet and Web pioneers awarded the inaugural Queen Elizabeth Prize for Engineering. </p>
