						<h2> Early Life </h2>
							<p>Andreessen was born in Cedar Falls, Iowa, and raised in New Lisbon, Wisconsin,[9] the son of Patricia and Lowell Andreessen, who worked for a seed company.[10] In December 1993,[9] he received his bachelor's degree in computer science from theUniversity of Illinois at Urbana-Champaign. As an undergraduate, he interned one summer at IBM in Austin, Texas. He also worked at the National Center for Supercomputing Applications (NCSA) at the University of Illinois, where he became familiar with Tim Berners-Lee's open standards for the World Wide Web. Andreessen and a full-time salaried co-worker Eric Bina worked on creating a user-friendly browser with integrated graphics that would work on a wide range of computers. The resulting code was the Mosaic Web browser.</p>


						<h2> Netscape </h2>
							<p>After his graduation from UIUC in 1993, Andreessen moved to California to work at Enterprise Integration Technologies. Andreessen then met with Jim Clark, the recently departed founder of Silicon Graphics. Clark believed the Mosaic browser had great commercial possibilities and suggested starting an Internet software company. Soon, Mosaic Communications Corporationwas in business in Mountain View, Santa Clara County, California, with Andreessen as cofounder and vice president of technology. The University of Illinois was unhappy with the company's use of the Mosaic name, so Mosaic Communications changed its name to Netscape Communications, and its flagship Web browser was the Netscape Navigator.
							In the year between the formation of the company and its IPO, Andreessen engaged in extensive public outreach on behalf of his vision of the Web browser's potential, something he had in fact done continuously since making the decision to distribute Mosaic for free via the Internet.
							One of these events, hosted by Internet commercialization pioneer Ken McCarthy, was captured on video [12] and provides a unique look at the state of the Web between the time Andreessen and his colleagues launched Mosaic and the time when Web browsers and servers became mainstream commercial products. At the time of the recording, Andreessen was 23 years old.
							Netscape's IPO in 1995 propelled Andreessen into the imagination of the public. Featured on the cover of Time[13][14] and other publications,[15] Andreessen became the poster-boy wunderkind of the Internet bubble generation: young, twenty-something, high-tech, ambitious, and worth millions (or billions) of dollars practically overnight.
							Netscape's success attracted the attention of Microsoft, which recognized the Web's potential and wanted to put itself at the forefront of the rising Internet revolution. Microsoft licensed the Mosaic source code from Spyglass, Inc., an offshoot of the University of Illinois, and turned it into Internet Explorer. The resulting battle between the two companies became known as theBrowser wars. In 1997, Andreessen was featured on the cover of Interactive Week Magazine
							Netscape was acquired in 1999 for $4.2 billion by AOL, which made Andreessen its Chief Technology Officer. The same year, he was named to the MIT Technology Review TR100 as one of the top 100 innovators in the world under the age of 35 </p>


						<h2> Loudcloud </h2>
							<p>Andreessen left Netscape to form Loudcloud with Ben Horowitz, Tim Howes, and In Sik Rhee.[17] Loudcloud was a services-based Web hosting company that made an IPO in 2001. Loudcloud sold its hosting business to EDS and changed its name toOpsware in 2003, with Andreessen serving as chairman. Opsware was purchased by Hewlett-Packard in September 2007 for about $1.6 billion.Between 2005 and 2009, Andreessen and longtime business partner Ben Horowitz separately and together invested $4 million in 45 start-ups that included Twitter and Qik.[18] The two became well known as super angel investors.[18] On July 6, 2009, Andreessen and Horowitz announced their Silicon Valley venture-capital firm Andreessen Horowitz.[19] The firm invests in entrepreneurs, products, and companies in the information technology industry.[20]
							Begun with an initial capitalization of $300 million,[21] within three years the firm grew to $2.7 billion under management across three funds.[22] Andreessen Horowitz’s portfolio holdings include Facebook, Foursquare, GitHub, Pinterest and Twitter.[23]
							On September 1, 2009, an investor group that included Andreessen Horowitz acquired a majority stake in Skype for $2.75 billion,[24] which was considered risky.[25] The deal paid off in May 2011 when Microsoft bought Skype for $8.5 billion.[25]Additionally, Andreessen and Horowitz made personal investments in headset maker Jawbone in 2006. The firm announced a $49-million investment in Jawbone in March 2011.[26] In February 2011, Andreessen Horowitz’s $80-million investment in Twitter[19] made it the first venture firm to hold stock in all four of the highest-valued, privately held social-media companies (at that time): Facebook, Groupon, Twitter, and Zynga.[27] </p>
